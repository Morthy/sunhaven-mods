    public static DecorationCategory GetCategoryId(string name)
    {
        return CategoryNames.FirstOrDefault(c => c.Value.Equals(name)).Key;
    }

    public static List<int> GetMatchingItemIds(DecorationCategory category, string search, int offset, int limit)
    {
        if (category == DecorationCategory.None || !Categories.TryGetValue(category, out var baseItems))
        {
            baseItems = GetAllItems();
        }

        var s = baseItems.Select(s => s);

        if (!search.Equals(""))
        {
            var splitSearch = search.Split(new[] { ' ' }, StringSplitOptions.RemoveEmptyEntries);
            s = s.Select(i => new { id = i, ItemDatabase.GetItemData(i).name })
                .Where(arg => splitSearch.Count(searchWord => arg.name.IndexOf(searchWord, StringComparison.OrdinalIgnoreCase) >= 0) == splitSearch.Length)
                .OrderBy(arg => LevenshteinDistance(search, arg.name))
                .ThenBy(arg => arg.name)
                .Select(arg => arg.id);
        }
        else
        {
            s = s.OrderBy(x => ItemDatabase.GetItemData(x).name);
        }

        return s.Skip(offset).Take(limit + 1).ToList();
    }
    

    private static List<int> GetAllItems()
    {
        if (_allItems == null)
        {
            var all = new List<int>();

            foreach (var x in Categories.Values)
            {
                all.AddRange(x);
            }

            _allItems = all.Distinct().ToList();
        }

        return _allItems;
    }
    
    private static int LevenshteinDistance(string s, string t) {
        // Special cases
        if (s == t) return 0;
        if (s.Length == 0) return t.Length;
        if (t.Length == 0) return s.Length;
        // Initialize the distance matrix
        int[][] distance = new int[s.Length + 1][];
        for (int index = 0; index < s.Length + 1; index++)
        {
            distance[index] = new int[t.Length + 1];
        }

        for (int i = 0; i <= s.Length; i++) distance[i][0] = i;
        for (int j = 0; j <= t.Length; j++) distance[0][j] = j;
        // Calculate the distance
        for (int i = 1; i <= s.Length; i++) {
            for (int j = 1; j <= t.Length; j++) {
                int cost = (s[i - 1] == t[j - 1]) ? 0 : 1;
                distance[i][j] = Math.Min(Math.Min(distance[i - 1][j] + 1, distance[i][j - 1] + 1), distance[i - 1][j - 1] + cost);
            }
        }
        // Return the distance
        return distance[s.Length][t.Length];
    }
    
